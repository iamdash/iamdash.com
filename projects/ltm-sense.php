        <div class="copy">
            <p>Working alongside a Flash developer, I built a content management system that was used internally at the London Transport Museum (LTM) to manage content and view data of the visitor's interactions for the interactive table.</p>
            <p>The CMS, installed and run on the museum's interval server allowed the client, LTM to upload text content as well as video, sound and images to the system. The content was then queried by the Flash interactive and served out as XML.</p>

